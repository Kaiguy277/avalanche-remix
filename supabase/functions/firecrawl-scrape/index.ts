import { getCorsHeaders, handleCorsPreflightRequest } from "../_shared/cors.ts";
import { validateString, validateObject, createValidationError } from "../_shared/validation.ts";

Deno.serve(async (req) => {
  // Handle CORS preflight
  const corsResponse = handleCorsPreflightRequest(req);
  if (corsResponse) return corsResponse;

  const origin = req.headers.get('origin');
  const corsHeaders = getCorsHeaders(origin);

  try {
    const body = await req.json();
    const { url, options } = body;

    // Validate URL input
    const urlValidation = validateString(url, 'url', { minLength: 1, maxLength: 2000 });
    if (!urlValidation.success) {
      return createValidationError(urlValidation.error!, corsHeaders);
    }

    // Validate options if provided
    const optionsValidation = validateObject(options, 'options', { required: false });
    if (!optionsValidation.success) {
      return createValidationError(optionsValidation.error!, corsHeaders);
    }

    const apiKey = Deno.env.get('FIRECRAWL_API_KEY');
    if (!apiKey) {
      console.error('FIRECRAWL_API_KEY not configured');
      return new Response(
        JSON.stringify({ success: false, error: 'Firecrawl connector not configured' }),
        { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    // Format URL
    let formattedUrl = urlValidation.data!.trim();
    if (!formattedUrl.startsWith('http://') && !formattedUrl.startsWith('https://')) {
      formattedUrl = `https://${formattedUrl}`;
    }

    console.log('Scraping URL:', formattedUrl);

    const validOptions = optionsValidation.data || {};
    const response = await fetch('https://api.firecrawl.dev/v1/scrape', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        url: formattedUrl,
        formats: (validOptions as Record<string, unknown>).formats || ['markdown'],
        onlyMainContent: (validOptions as Record<string, unknown>).onlyMainContent ?? true,
        waitFor: (validOptions as Record<string, unknown>).waitFor,
        location: (validOptions as Record<string, unknown>).location,
      }),
    });

    const data = await response.json();

    if (!response.ok) {
      console.error('Firecrawl API error:', data);
      return new Response(
        JSON.stringify({ success: false, error: data.error || `Request failed with status ${response.status}` }),
        { status: response.status, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    console.log('Scrape successful');
    return new Response(
      JSON.stringify(data),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('Error scraping:', error);
    const errorMessage = error instanceof Error ? error.message : 'Failed to scrape';
    return new Response(
      JSON.stringify({ success: false, error: errorMessage }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
